
# typecasting
#   <datatype>(<variable name>)

num = int(input("Enter a number : "))
print(f"num = {num}")
print(f"type of num = {type(num)}")

pi = float(input("Enter a pi value : "))
print(f"pi = {pi}")
print(f"type of pi = {type(pi)}")

str = input("Enter a string : ")
print(f"str = {str}")
print(f"type of str = {type(str)}")